        <section>
            <h2>Stress Management</h2>
            <p>Practice relaxation techniques such as deep breathing, meditation, and yoga to manage stress levels. Chronic stress can elevate blood pressure over time, so it's important to find ways to relax and unwind.</p>
        </section>

        <section>
            <h2>Adequate Sleep</h2>
            <p>Ensure you get 7-9 hours of quality sleep each night. Poor sleep can contribute to higher blood pressure, so it's crucial to maintain a healthy sleep routine.</p>
        </section>

        <section>
            <h2>Limit Alcohol and Caffeine</h2>
            <p>Consume alcohol in moderation, and limit your caffeine intake. Excessive alcohol consumption and high caffeine levels can raise your blood pressure.</p>
        </section>

        <section>
            <h2>Regular Health Monitoring</h2>
